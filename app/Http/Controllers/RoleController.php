    public function getDataRoles()
    {
        $roles = Role::all();

        return DataTables::of($roles)
            ->addColumn('action',function ($roles){
                return [
                    'edit' =>  route('admin.group.edit', $roles),
                    'delete' => route('admin.group.delete', $roles),
                ];
            })
            ->toJson();
    }

